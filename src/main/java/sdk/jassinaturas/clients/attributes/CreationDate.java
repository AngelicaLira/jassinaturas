package sdk.jassinaturas.clients.attributes;


public class CreationDate {
    private int day;
    private int hour;
    private int minute;
    private int month;
    private int second;
    private int year;

    public int getDay() {
        return day;
    }

    public int getHour() {
        return hour;
    }

    public int getMinute() {
        return minute;
    }

    public int getMonth() {
        return month;
    }

    public int getSecond() {
        return second;
    }

    public int getYear() {
        return year;
    }

    public void setDay(final int day) {
        this.day = day;
    }

    public void setHour(final int hour) {
        this.hour = hour;
    }

    public void setMinute(final int minute) {
        this.minute = minute;
    }

    public void setMonth(final int month) {
        this.month = month;
    }

    public void setSecond(final int second) {
        this.second = second;
    }

    public void setYear(final int year) {
        this.year = year;
    }

}
