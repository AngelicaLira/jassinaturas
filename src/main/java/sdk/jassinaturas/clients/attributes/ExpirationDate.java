package sdk.jassinaturas.clients.attributes;

public class ExpirationDate {
    private int day;
    private int month;
    private int year;

    public int getDay() {
        return day;
    }

    public int getMonth() {
        return month;
    }

    public int getYear() {
        return year;
    }

    public void setDay(final int day) {
        this.day = day;
    }

    public void setMonth(final int month) {
        this.month = month;
    }

    public void setYear(final int year) {
        this.year = year;
    }

}
